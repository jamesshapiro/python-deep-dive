# truncation
# floor
# ceiling
# rounding

print('Truncation simply ignores everything after the decimal point')

import math

print(f'{math.trunc(10.4)=}')
print(f'{math.trunc(-10.4)=}')

print(f'{int(10.4)=}')
print(f'{int(-10.4)=}')

print(f'{math.floor(10.4)=}')
print(f'{math.floor(-10.4)=}')

print(f'{math.ceil(10.4)=}')
print(f'{math.ceil(-10.4)=}')